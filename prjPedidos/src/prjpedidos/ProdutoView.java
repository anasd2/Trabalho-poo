private void apresentarListaNaTabela() {
        
        DefaultTableModel modelo = (DefaultTableModel) tblProd.getModel();
        modelo.setNumRows(0);
        for (Produto item: GerenciarPedidos.listaProduto ) {        
    
            modelo.addRow(new Object[]{
                item.codProd,
                item.nome,
                item.quantidade,
                item.valor
            });
        }
    }

    private void limparTudo() {
        for (Component item : this.getContentPane().getComponents()) {
            if(item instanceof JTextField){
                
                JTextField obj = (JTextField) item;
                //JOptionPane.showMessageDialog(null, obj.getName());
               if(!"id".equals(obj.getName()))  obj.setText(null);
            }
        }
    }





