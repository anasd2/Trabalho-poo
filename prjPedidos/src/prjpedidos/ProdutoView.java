    private void lblAddPRoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lblAddPRoActionPerformed
       
        try {
       Produto produto = new Produto();
       produto.codProd=Integer.parseInt(txtCodProd.getText());
       produto.nome = txtNomeProd.getText();
       produto.quantidade=Integer.parseInt(txtQuant.getText());
       produto.valor=Double.parseDouble(txtValor.getText());
       
       GerenciarPedidos.adicionarProduto(produto);
        apresentarListaNaTabela();
        limparTudo();
        txtCodProd.requestFocus();
        
        
      }catch (Exception e){
           JOptionPane.showMessageDialog(null, "Os campos devem ser preenchidos!");
          
      }       
       
    }//GEN-LAST:event_lblAddPRoActionPerformed

