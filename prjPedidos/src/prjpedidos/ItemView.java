    private void atualizaTotalPedido(){
        PedidosView.txtValorTotal.setText("");
        List<ItemPedido> itens = PedidosView.ped.lista;
        double totalItens = 0;
        for(int i = 0; i < itens.size(); i++){
            totalItens += itens.get(i).valorTotal;
        }
        PedidosView.txtValorTotal.setText(String.valueOf(totalItens));
    }
    
