    private void cmdAddITEMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAddITEMActionPerformed
       
        try {
        Produto p = new Produto();
        for (Produto pro : GerenciarPedidos.listaProduto) {
            if (cbProd.getSelectedItem().equals(pro.nome)){
                double r =pro.valor * Integer.parseInt(txtQTDD.getText());
                String tex = String.valueOf(r);
                txtTotalItem.setText(tex);
                if (pro.quantidade>=Integer.parseInt(txtQTDD.getText())){
                pro.quantidade-=Integer.parseInt(txtQTDD.getText());}
                else{
                   throw new SemProd ();    
                }
                p=pro;
            }
        }
        item = new ItemPedido();
        
        item.produto=p;
        item.quantidade=Integer.parseInt(txtQTDD.getText());
        item.valorTotal= Double.parseDouble(txtTotalItem.getText());
        PedidosView.ped.lista.add(item);
        PedidosView.itemWindow.setVisible(false);
        txtQTDD.setText("");
        txtTotalItem.setText("");
        PedidosView.apresentarListaNaTabela();
        //atualiza o total do pedido
        atualizaTotalPedido();
        }catch (NumberFormatException e){
           JOptionPane.showMessageDialog(null, "Os campos devem ser preenchidos");
          
      } catch (SemProd ex) {    
          JOptionPane.showMessageDialog(null, "Erro: " +ex.toString())  ;
        }    
        
    }//GEN-LAST:event_cmdAddITEMActionPerformed

