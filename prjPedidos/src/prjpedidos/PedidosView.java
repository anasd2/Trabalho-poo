static void apresentarListaNaTabela() {
        
        DefaultTableModel modelo = (DefaultTableModel) tblItemPedido.getModel();
        modelo.setNumRows(0);
        for (ItemPedido item: ped.lista ) {        
    
            modelo.addRow(new Object[]{
                item.produto.nome,
                item.quantidade,
                item.valorTotal
            });
        }
    }

    private void limparTudo() {
        for (Component item : this.getContentPane().getComponents()) {
            if(item instanceof JTextField){
                
                JTextField obj = (JTextField) item;
                //JOptionPane.showMessageDialog(null, obj.getName());
               if(!"id".equals(obj.getName()))  obj.setText(null);
            }
        }
    }



